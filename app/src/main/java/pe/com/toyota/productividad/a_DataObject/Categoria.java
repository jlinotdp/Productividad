public class Categoria {
    int codcateg;
    String codcat;
    String descat;

    public int getCodcateg() {
        return codcateg;
    }

    public void setCodcateg(int codcateg) {
        this.codcateg = codcateg;
    }

    public String getCodcat() {
        return codcat;
    }

    public void setCodcat(String codcat) {
        this.codcat = codcat;
    }

    public String getDescat() {
        return descat;
    }

    public void setDescat(String descat) {
        this.descat = descat;
    }
}
